#Tipe data boolean
x = True
print(x)
print(type(x))

#Tipe data string
x = "Aku suka python"
print(x)
print(type(x))

#Tipe data integer
x = 69
print(x)
print(type(x))

#Tipe data float
x = 3.22
print(x)
print(type(x))

#Tipe data hexadecimal
x = hex(20)
print(x)
print(type(x))

#Tipe data complex
x = (20j)
print(x)
print(type(x))