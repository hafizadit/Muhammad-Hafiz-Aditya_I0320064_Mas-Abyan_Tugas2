#Menampilkan judul
print("\n==============================\nKonversi Farenheit ke Celcius\n==============================\n")

#Input suhu
f = float(input("Masukkan suhu dalam farenheit :"))

#Konversi suhu
c = 5 * (f-32) / 9

#Menampilkan Informasi
print("Suhu dalam farenheit :", f,"derajat farenheit")
print("Suhu dalam celcius :", c,"derajat celcius")
print("\n==============================\n            Selesai\n==============================\n")